export function translate(locale: Locale, key: string, vars?: Record<string, string | number>) {
  const parts = key.split(".");
  let node: string | TranslationTree | undefined = translations[locale];
  for (const part of parts) {
    if (!node || typeof node === "string") {
      node = undefined;
      break;
    }
    node = node[part];
  }
  let text = typeof node === "string" ? node : key.split(".").pop()?.replace(/_/g, " ") || key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}
